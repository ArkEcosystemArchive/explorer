<template>
  <div class="max-w-2xl mx-auto md:pt-5">
    <ContentHeader>{{ $t("Top Wallets") }}</ContentHeader>
    <section class="page-section py-5 md:py-10">
      <div class="hidden sm:block">
        <TableWalletsDesktop
          :wallets="wallets"
          :total="supply"
          :sort-query="sortParams"
          @on-sort-change="onSortChange"
        />
      </div>
      <div class="sm:hidden">
        <TableWalletsMobile
          :wallets="wallets"
          :total="supply"
        />
      </div>
      <Paginator
        v-if="showPaginator"
        :previous="meta.previous"
        :next="meta.next"
        @previous="onPrevious"
        @next="onNext"
      />
    </section>
  </div>
</template>

<script type="text/ecmascript-6">
import WalletService from '@/services/wallet'
import { mapGetters } from 'vuex'

export default {
  data: () => ({
    wallets: null,
    meta: null,
    currentPage: 0
  }),

  computed: {
    ...mapGetters('network', ['supply']),

    showPaginator () {
      return this.meta && (this.meta.previous || this.meta.next)
    },

    sortParams: {
      get () {
        return this.$store.getters['ui/walletSortParams']
      },

      set (params) {
        this.$store.dispatch('ui/setWalletSortParams', {
          field: params.field,
          type: params.type
        })
      }
    }
  },

  watch: {
    currentPage () {
      this.changePage()
    }
  },

  created () {
    this.$on('paginatorChanged', page => this.changePage(page))
  },

  async beforeRouteEnter (to, from, next) {
    try {
      const { meta, data } = await WalletService.top(to.params.page)

      next(vm => {
        vm.currentPage = to.params.page
        vm.setWallets(data)
        vm.setMeta(meta)
      })
    } catch (e) { next({ name: '404' }) }
  },

  async beforeRouteUpdate (to, from, next) {
    this.wallets = null
    this.meta = null

    try {
      const { meta, data } = await WalletService.top(to.params.page)

      this.currentPage = to.params.page
      this.setWallets(data)
      this.setMeta(meta)
      next()
    } catch (e) { next({ name: '404' }) }
  },

  methods: {
    setWallets (wallets) {
      this.wallets = wallets
    },

    setMeta (meta) {
      this.meta = meta
    },

    onPrevious () {
      this.currentPage = Number(this.currentPage) - 1
    },

    onNext () {
      this.currentPage = Number(this.currentPage) + 1
    },

    changePage () {
      this.$router.push({
        name: 'top-wallets',
        params: {
          page: this.currentPage
        }
      })
    },

    onSortChange (params) {
      this.sortParams = params
    }
  }
}
</script>
