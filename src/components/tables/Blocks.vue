<template>
  <Loader :data="blocks">
    <TableWrapper
      v-bind="$attrs"
      :has-pagination="false"
      :columns="columns"
      :rows="blocks"
      :sort-query="{ field: 'height', type: 'desc' }"
      :no-data-message="$t('No results')"
    >
      <template
        slot-scope="data"
      >
        <div v-if="data.column.field === 'id'">
          <LinkBlock :id="data.row.id" />
        </div>

        <div v-else-if="data.column.field === 'height'">
          <span>
            {{ data.row.height }}
          </span>
        </div>

        <div v-else-if="data.column.field === 'timestamp'">
          <span>
            {{ data.formattedRow['timestamp'] }}
          </span>
        </div>

        <div v-else-if="data.column.field === 'transactions'">
          <span>
            {{ data.row.transactions }}
          </span>
        </div>

        <div v-else-if="data.column.field === 'generator'">
          <LinkWallet :address="data.row.generator.address" />
        </div>

        <div v-else-if="data.column.field === 'reward'">
          <span
            v-tooltip="{
              trigger: 'hover',
              content: readableCurrency(data.row.forged.total, data.row.price)
            }"
            class="whitespace-no-wrap"
          >
            {{ readableCrypto(data.row.forged.total) }}
          </span>
        </div>

        <div v-else-if="data.column.field === 'fee'">
          <span
            v-tooltip="{
              trigger: 'hover',
              content: data.row.forged.fee ? readableCurrency(data.row.forged.fee, data.row.price) : ''
            }"
            class="whitespace-no-wrap"
          >
            {{ readableCrypto(data.row.forged.fee) }}
          </span>
        </div>
      </template>
    </TableWrapper>
  </Loader>
</template>

<script type="text/ecmascript-6">
import CryptoCompareService from '@/services/crypto-compare'

export default {
  name: 'TableBlocksDesktop',

  props: {
    blocks: {
      validator: value => {
        return Array.isArray(value) || value === null
      },
      required: true
    }
  },

  computed: {
    columns () {
      const columns = [
        {
          label: this.$t('ID'),
          field: 'id',
          thClass: 'start-cell',
          tdClass: 'start-cell'
        },
        {
          label: this.$t('Height'),
          field: 'height',
          type: 'number',
          thClass: 'text-left hidden md:table-cell',
          tdClass: 'text-left hidden md:table-cell wrap-timestamp'
        },
        {
          label: this.$t('Timestamp'),
          field: 'timestamp',
          type: 'date',
          formatFn: this.formatDate,
          thClass: 'text-left hidden md:table-cell',
          tdClass: 'text-left hidden md:table-cell wrap-timestamp'
        },
        {
          label: this.$t('Transactions'),
          field: 'transactions',
          type: 'number'
        },
        {
          label: this.$t('Generated by'),
          field: 'generator',
          thClass: 'text-right',
          tdClass: 'text-right'
        },
        {
          label: this.$t('Reward (token)', { token: this.networkToken() }),
          field: 'reward',
          type: 'number'
        },
        {
          label: this.$t('Fee (token)', { token: this.networkToken() }),
          field: 'fee',
          type: 'number',
          thClass: 'end-cell',
          tdClass: 'end-cell'
        }
      ]

      return columns
    }
  },

  watch: {
    blocks () {
      this.updateBlocks()
    }
  },

  created () {
    this.updateBlocks()
  },

  methods: {
    formatDate (timestamp) {
      return this.readableTimestamp(timestamp.unix)
    },

    async updateBlocks () {
      if (!this.blocks) {
        return
      }

      for (const block of this.blocks) {
        block.price = await CryptoCompareService.dailyAverage(block.timestamp.unix)
      }
    }
  }
}
</script>

<style>
  .wrap-timestamp {
    white-space: normal;
  }

  @media(min-width: 870px) {
    .wrap-timestamp {
      white-space: nowrap;
    }
  }
</style>
